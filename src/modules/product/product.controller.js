import connection from "../../../db/models/connectionDB.js"
//1- add product.

export const addProduct = (req, res, next) => {
    const { name, category, unitPrice } = req.body;
    const query = `INSERT INTO product (name, category, unitPrice) VALUES ('${name}', '${category}', '${unitPrice}')`;
    connection.execute(query, (err, result) => {
        if (err) {
            return res.status(400).json({ message: "Error adding product", error: err });
        }
        return res.status(200).json("Product added successfully");
    });
}
